def normalize_style_name(style: str | None) -> str:
    key = (style or "default").strip().lower()
    key = key.replace("-", "_").replace(" ", "_")
    return COLOR_STYLE_ALIASES.get(key, key)


def builtin_color_styles() -> set[str]:
    return {*CONTINUOUS_COLOR_PALETTES, *DISCRETE_COLOR_PALETTES}


def _lerp_color(left: RGB, right: RGB, t: float) -> RGB:
    return tuple(round(left[i] + (right[i] - left[i]) * t) for i in range(3))  # type: ignore[return-value]


def _continuous_color(anchors: Sequence[ColorAnchor], t: float) -> RGB:
    for index in range(1, len(anchors)):
        left_t, left_color = anchors[index - 1]
        right_t, right_color = anchors[index]
        if t <= right_t:
            local_t = 0.0 if right_t == left_t else (t - left_t) / (right_t - left_t)
            return _lerp_color(left_color, right_color, max(0.0, min(1.0, local_t)))
    return anchors[-1][1]


def _discrete_color(colors: Sequence[RGB], index: int, levels: int) -> RGB:
    color_index = min(len(colors) - 1, (index * len(colors)) // levels)
    return colors[color_index]


def _channel_text(value: int) -> str:
    text = f"{value / 255.0:.6f}".rstrip("0").rstrip(".")
    return text or "0"


def _palette_line(index: int, color: RGB) -> str:
    r, g, b = color
    return f"{index} {_channel_text(r)} {_channel_text(g)} {_channel_text(b)}"


def palette_lines(style: str, levels: int) -> list[str]:
    lines: list[str] = []
    if style in CONTINUOUS_COLOR_PALETTES:
        anchors = CONTINUOUS_COLOR_PALETTES[style]
        for index in range(levels + 1):
            t = index / levels
            lines.append(_palette_line(index, _continuous_color(anchors, t)))
        return lines
    colors = DISCRETE_COLOR_PALETTES[style]
    for index in range(levels + 1):
        lines.append(_palette_line(index, _discrete_color(colors, index, levels)))
    return lines
